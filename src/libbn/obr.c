    /*vect_t vline, vz;*/
    if (!pnts || !p1 || !p2 || !p3 || !p4) return -1;
    /* Need 2d points for this to work */
    while (i < pnt_cnt) {
	if (!NEAR_ZERO(pnts[i][2], BN_TOL_DIST)) return -1;
	i++;
    }
    dim = pnt2d_array_get_dimension(pnts, pnt_cnt, &center, &pmin, &pmax);
